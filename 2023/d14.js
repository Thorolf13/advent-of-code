function pivot (array2D) {
  const newArray2D = []
  for (let i = 0; i < array2D.length; i++) {
    for (let j = 0; j < array2D[0].length; j++) {
      newArray2D[j] = newArray2D[j] || []
      newArray2D[j][i] = array2D[i][j]
    }
  }
  return newArray2D
}

function splitArr(arr, value) {
  const result = []
  let temp = []
  for (let i = 0; i < arr.length; i++) {
    if (arr[i] === value) {
      result.push(temp)
      temp = []
    } else {
      temp.push(arr[i])
    }
  }
  result.push(temp)
  return result
}

function joinArr(arrList, value) {
  const result = [...arrList[0]]
  for (let i = 1; i < arrList.length; i++) {
    result.push(value)
    result.push(...arrList[i])
  }
  return result
}

function rollTo(arr, dir){

  const arr2 = dir == 'north' || dir == 'south' ? pivot(arr) : arr.slice()
  const sens = dir == 'north' || dir == 'west' ? -1 : 1

  for (let i = 0; i < arr2.length; i++) {
    let col = arr2[i]
    let parts = splitArr(col, '#')
    parts = parts.map(p => p.sort((a, b) => a === 'O' ? sens : -sens))
    col = joinArr(parts, '#')
    arr2[i] = col
  }
  
  return dir == 'north' || dir == 'south' ? pivot(arr2) : arr2.slice()
}

