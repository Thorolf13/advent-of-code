const dirs = ['N', 'S', 'E', 'W', 'NE', 'NW', 'SE', 'SW']

function increment (x, y, dir) {
  switch (dir) {
    case 'N': return [x, y - 1]
    case 'S': return [x, y + 1]
    case 'E': return [x + 1, y]
    case 'W': return [x - 1, y]
    case 'NE': return [x + 1, y - 1]
    case 'NW': return [x - 1, y - 1]
    case 'SE': return [x + 1, y + 1]
    case 'SW': return [x - 1, y + 1]
  }
}

